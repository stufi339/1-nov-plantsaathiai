// Helper function to get message based on language and severity
export function getNotificationMessage(
  category: string,
  type: string,
  severity: 'high' | 'medium' | 'low' = 'medium',
  language: 'en' | 'hi' | 'bn' = 'en'
): string {
  try {
    const message = notificationMessages[category]?.[type];
    
    if (!message) {
      return 'Update available';
    }

    // If message has severity levels
    if (message[severity]) {
      return message[severity][language] || message[severity].en;
    }

    // If message is direct
    if (typeof message === 'object' && message[language]) {
      return message[language];
    }

    return message.en || 'Update available';
  } catch (error) {
    console.error('Error getting notification message:', error);
    return 'Update available';
  }
}

// Helper to get action button text
export function getActionText(
  action: string,
  language: 'en' | 'hi' | 'bn' = 'en'
): string {
  return notificationMessages.actions[action]?.[language] || 
         notificationMessages.actions[action]?.en || 
         'View';
}
